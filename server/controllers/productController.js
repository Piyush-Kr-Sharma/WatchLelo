export const createProductController = async (req, res) => {
  try {
    const { name, description, price, category, quantity, shipping } =
      req.fields; // contains non-file fields
    const { photo } = req.files; // contains file

    //validation
    switch (true) {
      case !name:
        return res.status(500).send({ error: "Name is Required" });
      case !description:
        return res.status(500).send({ error: "Description is Required" });
      case !price:
        return res.status(500).send({ error: "Price is Required" });
      case !category:
        return res.status(500).send({ error: "Category is Required" });
      case !quantity:
        return res.status(500).send({ error: "Quantity is Required" });
      case photo && photo.size > 1000000:
        return res
          .status(500)
          .send({ error: "photo is Required and should be less then 1mb" });
    }

    // Making a new product model
    const products = new productModel({ ...req.fields, slug: slugify(name) }); // ...req.fields -> req me jitna bhi fields hai unko spread kar denge
    // validating the photo
    if (photo) {
      // if we have photo in the product model then..
      products.photo.data = fs.readFileSync(photo.path); // photo object me se data leke fs module ki help se hum read kar lenge photo ko
      products.photo.contentType = photo.type;
    }
    await products.save(); // save the product model along with the photo
    res.status(201).send({
      success: true,
      message: "Product Created Successfully",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in crearing product",
    });
  }
};

